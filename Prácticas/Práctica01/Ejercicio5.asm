#include	<p16f877.inc>

K	EQU		H'20'

	ORG 	0
	GOTO 	INICIO
	ORG 	5

INICIO:		MOVLW	0X00
			MOVWF	K
SALTO1:		INCF	K,1
			MOVF 	K,W		;W <- K
			SUBLW	0X09
			BTFSS	STATUS,Z
			GOTO	NO_ES_IGUAL
SI_ES_IGUAL:
			MOVLW	0X10
			MOVWF	K
SALTO2:		INCF	K,1
			MOVF	K,W
			SUBLW	0X19
			BTFSS	STATUS,Z
			GOTO	NO_ES_IGUAL_2
			GOTO	ES_IGUAL_2	
NO_ES_IGUAL:
			GOTO	SALTO1
NO_ES_IGUAL_2:
			GOTO	SALTO2
ES_IGUAL_2:
			MOVLW	0X20
			MOVWF	K
			GOTO INICIO
			END	